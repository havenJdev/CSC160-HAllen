
public class MultSumResult {
	private double sum;
	private double product;
	
	
	public double getSum() {
		return sum;
	}
	public void setSum(double sum) {
		this.sum = sum;
	}
	public double getProduct() {
		return product;
	}
	public void setProduct(double product) {
		this.product = product;
	}

}
